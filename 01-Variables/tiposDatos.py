# Ejemplo de tipo de datos en Python

# Enteros
edad = 30
print(edad)

# Numero con punto flotante
altura = 1.752
print(altura)

# Cadena de texto
nombre = "Ana"
print(nombre)

# Tipo booleano
esEstudiante = False
print("Es estudiante?", esEstudiante)

# None, significa ausencia de valor
direccion = None
print("Direccion:", direccion)